# program to check if x is Equals 5, Greater than 4, Greater than or Equals 5, Less than 6, Less than or Equals 5, Not Equal 6 
x = 5
if x == 5 :
    print('Equals 5')
if x > 4 :
    print('Greater than 4')
if x >= 5 :
    print('Greater than or Equals 5')
if x < 6 :
    print('Less than 6')
if x <= 5 :
    print('Less than or Equals 5')
if x != 6 :
    print('Not Equal 6')       