# program to check if x is 5, still 5, Third 5, is 6, still 6, Third 6 
x = 5
print('Before 5')
if x == 5 :
    print('Is 5')
    print('Is still 5')
    print('Third 5')
print('Afterwards 5')
print('Before 6')
if x == 6 :
    print('Is 6')
    print('Is still 6')
    print('Third 6')
print('Afterwards 6')